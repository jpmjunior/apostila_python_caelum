# Função: velocidade_media()
#   distancia: metros
#   tempo: segundos
def velocidade_media(distancia, tempo):
    return distancia/tempo

print(velocidade_media(100,20))
print(velocidade_media(150,22))
print(velocidade_media(200,20))
print(velocidade_media(50,3))