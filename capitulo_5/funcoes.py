# Função: velocidade_media()
#   distancia: metros
#   tempo: segundos
def velocidade_media(distancia, tempo):
    velocidade = distancia/tempo
    print(velocidade)

velocidade_media(100,20)
velocidade_media(150,22)
velocidade_media(200,20)
velocidade_media(50,3)